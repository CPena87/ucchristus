function activateSlider(cat){
	
	category = cat;
	
	$.ajax({
		type: 'GET',
		url:"<?php echo get_bloginfo('url')?>/wp-admin/admin-ajax.php",
		dataType:"html",
		data:({ action : 'loadsArticles' , category : category }),
		success: function(data){
			
			
			
			console.log(category);
			
			$('.slider').html('');
			
			if(category == '3'){
				$('#slideLiderazgo').html(data);
				slide = 'slideLiderazgo';
			}
			else if(category == '5'){
				$('#slideEstress').html(data);
				slide = 'slideEstress'
			}
			else if(category == '4'){
				$('#slideEnergiza').html(data);
				slide = 'slideEnergiza';
				
			}
			
			var slider = $('#'+slide).bxSlider({
			  slideWidth: 500,
			  slideMargin:65,
			  minSlides: 3,
			  maxSlides: 3,
			  moveSlides: 1,
			  prevText:'<span class="fa fa-angle-left"></span>',
			  nextText:'<span class="fa fa-angle-right"></span>',
			  hideControlOnEnd:true,
			  pager:false,
			  responsive:true,
			  adaptiveHeight:true,
			});
			
			slider.destroySlider();
			
			slider.reloadSlider();
			
			
		}, 
		error : function(data){
			console.log('snap! no se pudo enviar tu pregunta')
			return false;
		}
	});
	
}
	
	
	
